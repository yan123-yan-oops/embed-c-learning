/**
 * 完整逻辑链：从存储到打印的 5 步
 * 
 * 第1步：计算机只存二进制，没有正负
 * 第2步：变量的类型决定怎么读这 8 个位
 * 第3步：%d 和 %u 是对提升后的 32 位值做解读
 * 第4步：整型提升（8位→32位）的补位规则
 * 第5步：%d vs %u 最终解读
 */

#include <stdio.h>
#include <stdint.h>

void print_bin_32(const char *label, uint32_t val)
{
    printf("  %-25s ", label);
    for (int i = 31; i >= 0; i--) {
        printf("%d", (val >> i) & 1);
        if (i % 8 == 0) printf(" ");
    }
    printf("\n");
}

void print_bin_8(const char *label, uint8_t val)
{
    printf("  %-25s ", label);
    for (int i = 7; i >= 0; i--) {
        printf("%d", (val >> i) & 1);
    }
    printf("\n");
}

int main(void)
{
    printf("===== 完整逻辑链：从存储到打印的 5 步 =====\n");
    printf("      以 int8_t(-56) 和 uint8_t(200) 为例\n\n");

    /* ============================================================
       第 1 步
       ============================================================ */
    printf("━━━ 第1步：计算机只存二进制，没有正负 ━━━\n\n");

    int8_t  sv = -56;
    uint8_t uv = 200;

    printf("  内存中的数据（两个变量存同样的位）：\n");
    print_bin_8("int8_t  sv = -56 的存储", (uint8_t)sv);
    print_bin_8("uint8_t uv = 200 的存储", uv);
    printf("\n  两个变量的存储值完全一样：1100 1000\n");
    printf("  这 8 个位就是 8 个高低电平，自己不知道正负\n\n");

    /* ============================================================
       第 2 步
       ============================================================ */
    printf("━━━ 第2步：变量的类型决定怎么读这 8 个位 ━━━\n\n");

    printf("  int8_t 从 1100 1000 中读出的逻辑值 = %d\n", sv);
    printf("    · 最高位是 1 → 负数\n");
    printf("    · 补码解码：绝对值 = 取反+1 = 00110111+1 = 56\n");
    printf("    · 所以是 -56\n\n");

    printf("  uint8_t 从 1100 1000 中读出的逻辑值 = %u\n", uv);
    printf("    · 直译：128 + 64 + 8 = 200\n\n");

    printf("  ✅ 验证：同一份存储值，两种解读 → 两个不同的逻辑值\n\n");

    /* ============================================================
       第 3 步 + 第 4 步
       ============================================================ */
    printf("━━━ 第3步+第4步：printf前的整型提升（8位→32位）━━━\n\n");

    printf("  传给 printf 时，8 位先提升为 32 位 int：\n\n");

    int promoted_sv = sv;  // 编译器自动做整型提升
    int promoted_uv = uv;

    print_bin_8("int8_t sv 原始 8 位", (uint8_t)sv);
    print_bin_32("提升为 32 位", promoted_sv);
    printf("    ↑ 有符号 → 高位补符号位(1)\n\n");

    print_bin_8("uint8_t uv 原始 8 位", uv);
    print_bin_32("提升为 32 位", promoted_uv);
    printf("    ↑ 无符号 → 高位补 0\n\n");

    /* ============================================================
       第 5 步
       ============================================================ */
    printf("━━━ 第5步：printf 用 %%d / %%u 解读这个 32 位值 ━━━\n\n");

    printf("  变量    提升后的32位                     %%d 解读   %%u 解读\n");
    printf("  ────────────────────────────────────────────────────────────\n");

    printf("  int8_t  ");
    print_bin_32("", promoted_sv);
    printf("                                          %-8d %-8u\n", sv, sv);

    printf("  uint8_t ");
    print_bin_32("", promoted_uv);
    printf("                                          %-8d %-8u\n", uv, uv);

    printf("\n  %%d 按有符号读 → int8_t 输出 -56（保留原值）\n");
    printf("  %%u 按无符号读 → int8_t 输出大数（陷阱！）\n");
    printf("  两者用无符号都正确，因为 200 是正数\n\n");

    /* ============================================================
       总结
       ============================================================ */
    printf("━━━ 完整链条 ━━━\n\n");
    printf("  内存(1100 1000)\n");
    printf("    ↓\n");
    printf("  类型决定读法:\n");
    printf("    int8_t  → -56    uint8_t → 200\n");
    printf("    ↓\n");
    printf("  printf 整型提升:\n");
    printf("    有符号 → 补符号位    无符号 → 补 0\n");
    printf("    ↓\n");
    printf("  %%d / %%u 最终解读:\n");
    printf("    %%d 有符号读    %%u 无符号读\n\n");
    printf("  符号位补码规律：无符号补 0，有符号补符号位\n");

    return 0;
}
